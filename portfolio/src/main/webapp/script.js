/**
 * Adds a random greeting to the page.
 */
function addRandomGreeting() {
  const greetings =
      ['Yoga', 'Crocheting', 'Walking my dog', 'Eating pizza'];

  // Pick a random greeting.
  const greeting = greetings[Math.floor(Math.random() * greetings.length)];

  // Add it to the page.
  const greetingContainer = document.getElementById('greeting-container');
  greetingContainer.innerText = greeting;
}

const githubs = ["https://github.com/vasuchal/Rate-My-Apartment", "https://github.com/hack4impact-uiuc/falling-fruit", "https://github.com/vasuchal/Dynamic-Drone-Sim-Audio-Visualizer"]

let slideIndex = 1;
showSlides(slideIndex);

// Next/previous controls
function plusSlides(n) {
  showSlides(slideIndex += n);
}

// Thumbnail image controls
function currentSlide(n) {
  showSlides(slideIndex = n);
}

function showSlides(n) {
  let i;
  let slides = document.getElementsByClassName("mySlides");
  let dots = document.getElementsByClassName("dot");
  if (n > slides.length) {slideIndex = 1}
  if (n < 1) {slideIndex = slides.length}
  for (i = 0; i < slides.length; i++) {
    slides[i].style.display = "none";
  }
  for (i = 0; i < dots.length; i++) {
    dots[i].className = dots[i].className.replace(" active", "");
  }
  slides[slideIndex-1].style.display = "block";
  dots[slideIndex-1].className += " active";
  document.getElementById('gitlink').innerHTML = githubs[slideIndex-1];
  document.getElementById("gitlink").href=githubs[slideIndex-1]; 

}

